// Accessing Array Elements

let fruits = ["apple", "mango", "grapes"];

console.log("Access the Full Array", fruits);
console.log("Accessing first Array Elements using Index number =", fruits[0]);
console.log("Accessing Array Elements using Index number =", fruits[1]);
console.log("Accessing Array Elements using Index number =", fruits[2]);

// Changing an Array Element

fruits[0] = "banana";
console.log("changing array element=", fruits);
console.log("Access the Full Array after changes=", fruits);

// The length Property
console.log("The length of array=", fruits.length);

// Arrays are Objects
console.log("Arrays are Objects (check)=", typeof fruits);
